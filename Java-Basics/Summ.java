/*
 Sum of two number a and b and taking the input from the user!!
 */
import java.util.*;
public class Summ {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a + b;
        System.out.println(sum);
    }
}
